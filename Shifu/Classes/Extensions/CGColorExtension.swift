import Foundation


public extension CGColor{
    static let white = { UIColor.white.cgColor }()
    static let green = { UIColor.green.cgColor }()
    static let red = { UIColor.red.cgColor }()
    static let blue = { UIColor.blue.cgColor }()
    static let yellow = { UIColor.yellow.cgColor }()
}
